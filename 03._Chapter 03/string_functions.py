# String functions are the functions designed pecifically to carry out specific functionalities according 
# to the requirement like we will be discussing it through different examples
name = "Zeeshan"

# len() function will be printing the length of the string 
print (len(name))

# This function will be printing out the string in upper case 
print(name.upper())

# This function will be printing out the string in lower case 
print(name.lower())

# This function will be checking whether the string starts with Zees or not by giving the answer in T or F
print(name.startswith("Zees"))